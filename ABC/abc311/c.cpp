template<typename T_n> T_n modinv(T_n a, T_n m) { T_n b = m, u = 1, v = 0; while (b) { T_n t = a / b; a -= t * b; swap(a, b); u -= t * v; swap(u, v); } u %= m; if (u < 0) u += m; return u; }
// 探索
vector<bool> seen, finished;

// サイクル復元のための情報
int pos = -1; // サイクル中に含まれる頂点 pos
stack<int> hist; // 訪問履歴

void dfs(const Graph &G, int v, int p) {
    seen[v] = true;
    hist.push(v);
    for (auto nv : G[v]) {
        // if (nv == p) continue; // 逆流を禁止する

        // 完全終了した頂点はスルー
        if (finished[nv]) continue;

        // サイクルを検出
        if (seen[nv] && !finished[nv]) {
            pos = nv;
            return;
        }

        // 再帰的に探索
        dfs(G, nv, v);

        // サイクル検出したならば真っ直ぐに抜けていく
        if (pos != -1) return;
    }
    hist.pop();
    finished[v] = true;
}

int main(){
    int n; cin >> n;
    Graph G(n);
    rep(i,n){
        int a; cin >> a;
        a--;
        G[i].push_back(a);
    }

    // if(n == 2){
    //     cout << 2 << endl;
    //     cout << 1 << " " << 2 << endl;
    //     return 0;
    // }

    // 探索
    seen.assign(n, false), finished.assign(n, false);
    pos = -1;
    
    rep(i,n){
        if(!seen[i]) {
            dfs(G, 0, -1);
            if(hist.size() != 0) break;
        }
    }

    // サイクルを復元
    vector<int> cycle;
    while (!hist.empty()) {
        int t = hist.top();
        cycle.push_back(t);
        hist.pop();
        if (t == pos) break;
    }
    reverse(cycle.begin(), cycle.end());
    cout << cycle.size() <<endl;
    for(auto x : cycle) {
        cout << x+1 << " ";
    }
    cout << endl;

    return 0;
}